//Assigment Operators

var x = 5; //Equal sign is the assignment operator

// Addition Assignments

x += 1; //x = x + 1

//Subtraction assignment

x -=1; //x = x -1

//Multiplaication

x *= 1; // x = x * 1

//Division
x /= 1; // x = x / 1


//Remainder (or Modulus) assignment
x %= 1; // x = x % 1;

// Exponential assignment
x **= 1; // x = x ** 1